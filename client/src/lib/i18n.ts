export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.fr;

export function getTranslation(
  language: Language,
  key: string
): string {
  const keys = key.split('.');
  let value: any = translations[language];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
}
